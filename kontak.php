$response = array();

if ($_SERVER["REQUEST_METHOD"] == "POST") {
    // Database configuration
    $servername = "localhost";
    $username = "root";
    $password = "";
    $dbname = "mahakam";

    // Create connection
    $conn = new mysqli($servername, $username, $password, $dbname);

    // Check connection
    if ($conn->connect_error) {
        $response['status'] = 'error';
        $response['message'] = 'Connection failed: ' . $conn->connect_error;
    } else {
        // Get form data
        $name = $conn->real_escape_string($_POST['nama']);
        $email = $conn->real_escape_string($_POST['email']);
        $phone = $conn->real_escape_string($_POST['telepon']);
        $subject = $conn->real_escape_string($_POST['subjek']);
        $message = $conn->real_escape_string($_POST['pesan']);

        // Insert data into database
        $sql = "INSERT INTO kontak (nama, email, telepon, subjek, pesan) VALUES ('$name', '$email', '$phone', '$subject', '$message')";

        if ($conn->query($sql) === TRUE) {
            $response['status'] = 'success';
            $response['message'] = 'Message sent successfully.';
        } else {
            $response['status'] = 'error';
            $response['message'] = 'Error: ' . $sql . '<br>' . $conn->error;
        }
    }

    // Close connection
    
    echo "<script>alert('Data berhasil ditambahkan');</script>";
}

